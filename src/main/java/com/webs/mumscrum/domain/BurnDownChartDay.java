package com.webs.mumscrum.domain;

public class BurnDownChartDay {
	private Integer sprintDay;
	private Integer remainingEffort;

	public Integer getSprintDay() {
		return sprintDay;
	}

	public void setSprintDay(Integer sprintDay) {
		this.sprintDay = sprintDay;
	}

	public Integer getRemainingEffort() {
		return remainingEffort;
	}

	public void setRemainingEffort(Integer remainingEffort) {
		this.remainingEffort = remainingEffort;
	}
}
